DROP DATABASE IF EXISTS LEAGUE_BD;
CREATE DATABASE IF NOT EXISTS LEAGUE_BD;
USE LEAGUE_BD;

--
-- Table structure for table `CHAMPION`
--

DROP TABLE IF EXISTS `CHAMPION`;
CREATE TABLE `CHAMPION` (
    `ChampionID` smallint(3) NOT NULL AUTO_INCREMENT,
    `Name` varchar(63) NOT NULL,
    `Sex` char(1) DEFAULT NULL,
    `ReleaseDate` date NOT NULL,
    `LocalOfBirth` varchar(63) DEFAULT NULL,
    PRIMARY KEY (`ChampionID`)
);

--
-- Table structure for table `REGION`
--

DROP TABLE IF EXISTS `REGION`;
CREATE TABLE `REGION` (
    `RegionID` varchar(2) NOT NULL,
    `Name` varchar(63) NOT NULL,
    `PReligion` varchar(255) DEFAULT NULL,
    `PMilitary` varchar(255) DEFAULT NULL,
    `PEconomy` varchar(255) DEFAULT NULL,
    `Governor` smallint(3) DEFAULT NULL,
    PRIMARY KEY (`RegionID`),
    FOREIGN KEY (`Governor`)
     REFERENCES `CHAMPION`(`ChampionID`)
);

--
-- Table structure for table `CLASSGROUP`
--

DROP TABLE IF EXISTS `CLASSGROUP`;
CREATE TABLE `CLASSGROUP` (
    `ClassGroup` varchar(63) NOT NULL,
    `Role` varchar(63) NOT NULL,
    `Function` varchar(255) NOT NULL,
    PRIMARY KEY(`ClassGroup`,`Role`)
);

--
-- Table structure for table `CLASS`
--

DROP TABLE IF EXISTS `CLASS`;
CREATE TABLE `CLASS` (
    `ClassName` varchar(63) NOT NULL,
    `MainDamage` varchar(63) NOT NULL,
    `ClassGroup` varchar(63) NOT NULL,
    PRIMARY KEY(`ClassName`),
    FOREIGN KEY(`ClassGroup`) REFERENCES `CLASSGROUP`(`ClassGroup`)
);

--
-- Table structure for table `ITEMS`
--

DROP TABLE IF EXISTS `ITEMS`;
CREATE TABLE `ITEMS`(
    `Item` varchar(63) NOT NULL,
    `ClassName` varchar(63) NOT NULL,
    PRIMARY KEY(`Item`,`ClassName`),
    FOREIGN KEY(`ClassName`) REFERENCES `CLASS`(`ClassName`)
);

--
-- Table structure for table `CHAMPIONCLASSES`
--

DROP TABLE IF EXISTS `CHAMPIONCLASSES`;
CREATE TABLE `CHAMPIONCLASSES`(
    `ChampionID` smallint(3) NOT NULL,
    `ClassName` varchar(63) NOT NULL,
    PRIMARY KEY(`ChampionID`, `ClassName`),
    FOREIGN KEY(`ChampionID`) REFERENCES `CHAMPION`(`ChampionID`),
    FOREIGN KEY(`ClassName`) REFERENCES `CLASS`(`ClassName`)
);

--
-- Table structure for table `RELATIONSHIPS`
--

DROP TABLE IF EXISTS `RELATIONSHIPS`;
CREATE TABLE `RELATIONSHIPS`(
    `InitialChampion` smallint NOT NULL,
    `FinalChampion` smallint NOT NULL,
    `Relation` varchar(63) NOT NULL,
    PRIMARY KEY(`InitialChampion`, `FinalChampion`, `Relation`),
    FOREIGN KEY(`InitialChampion`) REFERENCES `CHAMPION`(`ChampionID`),
    FOREIGN KEY(`FinalChampion`) REFERENCES `CHAMPION`(`ChampionID`)
);


